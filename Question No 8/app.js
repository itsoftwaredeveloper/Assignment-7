var char = String(input);
if (char == "a" || char == "e" || char == "i" || char == "o" || char == "u") {
    console.log("The Character is a Vowel.");
}
else if (char == "A" || char == "E" || char == "I" || char == "O" || char == "U") {
    console.log("The Character is a Vowel.");
}
else {
    console.log("The Character is a Consonant. ");
}
